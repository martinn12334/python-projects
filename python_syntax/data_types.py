# Data Types

# String
print("Hello"[0]) # printing a first character of this word
print("Hello"[4]) # printing a last character of this word
print("123" + "456") # concatenate
print("Hello " + "Martin " + "Ristov!")

# Integer
print(123) # printing a number
print(456 + 123) # addition of two numbers

# Large Number
print(123_456_789) # result will be 123456789
print(987,654,321) # result will be 987 654 321

# Float
print(3.14159) # print a number with dot

# Boolean -> only two results
print(True)
print(False)
